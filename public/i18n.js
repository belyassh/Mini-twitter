let currentLanguage = localStorage.getItem('language') || 'ru';

function t(key) {
    return translations[currentLanguage]?.[key] || translations['ru'][key] || key;
}

function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('language', lang);
        updatePageLanguage();
    }
}

function updatePageLanguage() {
    // Update navigation tabs
    const tabFeed = document.getElementById('tab-feed');
    const tabMyFeed = document.getElementById('tab-my-feed');
    const tabProfile = document.getElementById('tab-profile');
    const tabNotif = document.getElementById('tab-notifications');
    
    if (tabFeed) tabFeed.textContent = t('general-feed');
    if (tabMyFeed) tabMyFeed.textContent = t('my-feed');
    if (tabProfile) tabProfile.textContent = t('profile');
    if (tabNotif) tabNotif.textContent = t('notifications');

    // Update theme button
    const body = document.body;
    const btn = document.getElementById('theme-btn');
    if (btn) {
        if (body.classList.contains('light-theme')) {
            btn.innerText = t('dark-theme');
        } else {
            btn.innerText = t('light-theme');
        }
    }

    // Update write tweet section
    if (document.querySelector('h2')) {
        const headings = document.querySelectorAll('h2');
        if (headings[0]) headings[0].textContent = t('write-tweet');
    }

    // Update placeholders
    const tweetArea = document.getElementById('tweet-content');
    if (tweetArea) tweetArea.placeholder = t('tweet-placeholder');

    const imageInput = document.getElementById('tweet-image');
    if (imageInput) imageInput.placeholder = t('image-url-placeholder');

    // Update buttons
    const tweetBtn = document.querySelector('button.btn-primary[onclick="createTweet()"]');
    if (tweetBtn) tweetBtn.textContent = t('tweet-btn');

    // Update auth section
    const usernameInput = document.getElementById('reg-username');
    if (usernameInput) usernameInput.placeholder = t('username-placeholder');

    const emailInput = document.getElementById('reg-email');
    if (emailInput) emailInput.placeholder = t('email-placeholder');

    const passwordInput = document.getElementById('reg-password');
    if (passwordInput) passwordInput.placeholder = t('password-placeholder');

    // Update auth section heading and buttons
    const authSection = document.getElementById('auth-section');
    if (authSection) {
        const h2 = authSection.querySelector('h2');
        if (h2) h2.textContent = t('login-register');
        
        const buttons = authSection.querySelectorAll('button.btn-primary');
        if (buttons[0]) buttons[0].textContent = t('register-btn');
        if (buttons[1]) buttons[1].textContent = t('login-btn');
    }

    // Update feed title
    const feedTitle = document.getElementById('feed-title');
    if (feedTitle) {
        if (typeof currentTab !== 'undefined' && currentTab === 'my-feed') {
            feedTitle.textContent = t('my-feed-title');
        } else {
            feedTitle.textContent = t('general-feed');
        }
    }

    // Update profile section heading
    const profileSection = document.getElementById('profile-section');
    if (profileSection) {
        const h3 = profileSection.querySelector('h3');
        if (h3) h3.textContent = t('user-posts');
    }

    // Update notifications section heading
    const notificationsSection = document.getElementById('notifications-section');
    if (notificationsSection) {
        const h2 = notificationsSection.querySelector('h2');
        if (h2) h2.textContent = t('messages');
    }

    // Update follow button text
    const followBtn = document.getElementById('follow-btn');
    if (followBtn && followBtn.style.display !== 'none') {
        followBtn.textContent = t('follow-btn');
    }

    // Update profile counters
    const profileHeader = document.querySelector('.profile-header');
    if (profileHeader) {
        const counters = profileHeader.querySelector('.profile-counters');
        if (counters) {
            const divs = counters.querySelectorAll('div');
            if (divs[0]) {
                const followersSpan = divs[0].querySelector('span');
                if (followersSpan) {
                    const followersCount = followersSpan.textContent;
                    divs[0].innerHTML = `${t('followers')}: <span>${followersCount}</span>`;
                }
            }
            if (divs[1]) {
                const followingSpan = divs[1].querySelector('span');
                if (followingSpan) {
                    const followingCount = followingSpan.textContent;
                    divs[1].innerHTML = `${t('following')}: <span>${followingCount}</span>`;
                }
            }
        }
    }
}
